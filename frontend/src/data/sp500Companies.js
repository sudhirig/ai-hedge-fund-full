// Utility function to get company info by symbol
export const getCompanyInfo = (symbol) => {
  return SP500_COMPANIES.find(company => company.symbol === symbol);
};

// Utility function to filter companies by sector
export const getCompaniesBySector = (sector) => {
  return SP500_COMPANIES.filter(company => company.sector === sector);
};

// Get all unique sectors
export const getSectors = () => {
  return [...new Set(SP500_COMPANIES.map(company => company.sector))].sort();
};
